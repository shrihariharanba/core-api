package com.stpl.product.global.modal;

/**
 * @author shrihariharan
 *
 */
public class CustomerMaster {
	
	int customerSystemId;
	
	String customerId;
	
	String customerNo;
	
	String customerName;

	public int getCustomerSystemId() {
		return customerSystemId;
	}

	public void setCustomerSystemId(int customerSystemId) {
		this.customerSystemId = customerSystemId;
	}

	public String getCustomerId() {
		return customerId;
	}

	public void setCustomerId(String customerId) {
		this.customerId = customerId;
	}

	public String getCustomerNo() {
		return customerNo;
	}

	public void setCustomerNo(String customerNo) {
		this.customerNo = customerNo;
	}

	public String getCustomerName() {
		return customerName;
	}

	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}
	

}
